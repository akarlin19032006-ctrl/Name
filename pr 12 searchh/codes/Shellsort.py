def shell_sort(arr):
    """
    Сортировка Шелла
    """
    n = len(arr)
    # Начинаем с большого промежутка, затем уменьшаем его
    gap = n // 2
    
    # Пока промежуток больше 0
    while gap > 0:
        # Выполняем сортировку вставками с заданным промежутком
        for i in range(gap, n):
            temp = arr[i]
            j = i
            
            # Сдвигаем элементы, пока не найдем правильную позицию
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap
            
            # Вставляем элемент на правильную позицию
            arr[j] = temp
        
        # Уменьшаем промежуток
        gap //= 2
    
    return arr

# Демонстрация работы
if __name__ == "__main__":
    arr = [12, 34, 54, 2, 3, 8, 1, 23, 45, 21]
    print("Исходный массив:", arr)
    
    shell_sort(arr)
    print("Отсортированный массив:", arr)
